# Criando dois vetores de tamanhos diferentes
vetor1 <- c(1, 2, 3, 4)
vetor2 <- c(10, 20)

# Realizando uma operação aritmética entre os vetores
resultado <- vetor1 + vetor2

# Exibindo os vetores e o resultado
print(paste("Vetor 1: ", toString(vetor1)))
print(paste("Vetor 2: ", toString(vetor2)))
print(paste("Resultado (soma com reciclagem): ", toString(resultado)))
