# Criando um vetor com 2 números inteiros positivos
vetor <- c(16, 81)

# Calculando a raiz quadrada de cada elemento do vetor
raizes <- sqrt(vetor)

# Aplicando a função de logaritmo natural aos resultados das raízes quadradas
logaritmos <- log(raizes)

# Exibindo os resultados
print("Vetor original:")
print(vetor)

print("Raízes quadradas:")
print(raizes)

print("Logaritmo das raízes quadradas:")
print(logaritmos)
